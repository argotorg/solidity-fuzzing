#!/usr/bin/env bash
# Build the libFuzzer OSS-Fuzz harnesses natively (no Docker).
#
# The fuzz world is built with clang + libc++ (-stdlib=libc++): clang + the
# system libstdc++ SEGVs inside libstdc++'s dynamic_cast during every parse (see
# cmake/toolchains/libfuzzer-native.cmake). Because libc++ and libstdc++ have
# incompatible C++ ABIs, every C++ dependency that crosses the boundary must
# also be libc++, so — unlike a plain distro build — we cannot use the system
# libstdc++ boost/protobuf/abseil. Instead this script builds boost,
# protobuf+abseil (via libprotobuf-mutator's bundled copy), evmone-standalone
# and libprotobuf-mutator with -stdlib=libc++ into deps/, and the cmake build
# links against those. For the same ABI reason the libFuzzer engine itself is
# built from compiler-rt source against libc++ (the distro libclang_rt.fuzzer.a
# is libstdc++) — see build_libfuzzer below.
#
# Requirements (Arch package names in parentheses):
#   clang/clang++         (clang)        — libFuzzer is clang-only
#   libc++/libc++abi      (libc++)       — the C++ standard library we build against
#   abseil headers        (not needed — abseil is built from source here)
#   cmake, make, ninja, git, wget, b2/bootstrap (from the boost tarball)
set -ex

ROOTDIR="$(realpath "$(dirname "$0")/..")"
BUILDDIR="${ROOTDIR}/build_ossfuzz"
DEPS="${ROOTDIR}/deps"
LPM_GIT="https://github.com/google/libprotobuf-mutator.git"
BOOST_VERSION="1.83.0"

export CC="${CC:-clang}"
export CXX="${CXX:-clang++}"

# Every C++ dependency below is compiled against libc++ so its ABI matches the
# fuzz binaries (see the file header).
LIBCXX_CXXFLAGS="-stdlib=libc++"
LIBCXX_LDFLAGS="-stdlib=libc++"

mkdir -p "${BUILDDIR}" "${DEPS}" "${DEPS}/src" "${DEPS}/bin" "${DEPS}/include" "${DEPS}/lib"

# Boost, built static against libc++. Solidity links boost_filesystem /
# boost_system / boost_program_options; those archives must be libc++ to match.
function build_boost
{
  if [[ -f "${DEPS}/lib/libboost_filesystem.a" ]]; then
    return
  fi
  local uver="${BOOST_VERSION//./_}"
  local src="${DEPS}/src/boost_${uver}"
  if [[ ! -f "${src}/bootstrap.sh" ]]; then
    wget -q "https://archives.boost.io/release/${BOOST_VERSION}/source/boost_${uver}.tar.bz2" \
      -O "${DEPS}/boost.tar.bz2"
    tar -xf "${DEPS}/boost.tar.bz2" -C "${DEPS}/src"
    rm -f "${DEPS}/boost.tar.bz2"
  fi
  cd "${src}"
  ./bootstrap.sh --with-toolset=clang --prefix="${DEPS}"
  ./b2 toolset=clang \
    cxxflags="-std=c++20 ${LIBCXX_CXXFLAGS}" \
    linkflags="${LIBCXX_LDFLAGS}" \
    link=static variant=release threading=multi runtime-link=shared \
    --with-system --with-filesystem --with-program_options --with-test \
    -j"$(nproc)" install
}

# libprotobuf-mutator + the protobuf/abseil it bundles, all built against libc++.
# DOWNLOAD_PROTOBUF=ON makes LPM build its own protobuf (and abseil); we then
# stage that protoc + headers + static libs (and their cmake package configs)
# into deps/ so the harnesses link the same libc++ protobuf and the bindings are
# generated by the matching protoc.
function build_protobuf_and_lpm
{
  if [[ -f "${DEPS}/lib/libprotobuf-mutator.a" && -x "${DEPS}/bin/protoc" ]]; then
    return
  fi
  local src="${DEPS}/src/libprotobuf-mutator"
  local build="${DEPS}/build/libprotobuf-mutator"
  if [[ ! -d "${src}/.git" ]]; then
    git clone --depth 1 "${LPM_GIT}" "${src}"
  fi
  # CXXFLAGS/CFLAGS in the env so the protobuf ExternalProject also picks up
  # -stdlib=libc++ (the OSS-Fuzz mechanism), in addition to CMAKE_CXX_FLAGS.
  CXXFLAGS="${LIBCXX_CXXFLAGS}" CFLAGS="" \
  cmake -S "${src}" -B "${build}" \
    -DCMAKE_BUILD_TYPE=Release \
    -DCMAKE_C_COMPILER="${CC}" -DCMAKE_CXX_COMPILER="${CXX}" \
    -DCMAKE_CXX_STANDARD=20 \
    -DCMAKE_CXX_FLAGS="${LIBCXX_CXXFLAGS}" \
    -DCMAKE_EXE_LINKER_FLAGS="${LIBCXX_LDFLAGS}" \
    -DLIB_PROTO_MUTATOR_DOWNLOAD_PROTOBUF=ON \
    -DLIB_PROTO_MUTATOR_TESTING=OFF \
    -DLIB_PROTO_MUTATOR_EXAMPLES=OFF \
    -DCMAKE_INSTALL_PREFIX="${DEPS}"
  cmake --build "${build}" -j"$(nproc)"
  cp -rp "${build}/external.protobuf/bin/." "${DEPS}/bin/"
  cp -rp "${build}/external.protobuf/include/." "${DEPS}/include/"
  cp -rp "${build}/external.protobuf/lib/." "${DEPS}/lib/"
  cmake --install "${build}"
}

# evmone as a self-contained static archive, instrumented like the harnesses and
# built against libc++. Linked directly into the fuzzers (no libevmone.so at
# runtime), so its ABI must match too.
function build_evmone_standalone
{
  if [[ -f "${DEPS}/lib/libevmone-standalone.a" ]]; then
    return
  fi
  local build="${DEPS}/build/evmone"
  cmake -S "${ROOTDIR}/evmone" -B "${build}" \
    -DCMAKE_BUILD_TYPE=Release \
    -DCMAKE_C_COMPILER="${CC}" -DCMAKE_CXX_COMPILER="${CXX}" \
    -DBUILD_SHARED_LIBS=OFF \
    -DEVMONE_TESTING=OFF -DBUILD_TESTING=OFF \
    -DCMAKE_INSTALL_PREFIX="${DEPS}" \
    -DCMAKE_EXE_LINKER_FLAGS="${LIBCXX_LDFLAGS}" \
    -DCMAKE_CXX_FLAGS="${LIBCXX_CXXFLAGS} -fsanitize=fuzzer-no-link -fsanitize=undefined -fno-omit-frame-pointer -gline-tables-only -DFUZZING_BUILD_MODE_UNSAFE_FOR_PRODUCTION -D_LIBCPP_HARDENING_MODE=_LIBCPP_HARDENING_MODE_EXTENSIVE"
  cmake --build "${build}" -j"$(nproc)"
  cmake --install "${build}"
}

function generate_protobuf_bindings
{
  # Regenerated on every build with the protoc we just built (deps/bin/protoc)
  # so the bindings match the libc++ libprotobuf we link. Committed only so the
  # LSP/IDE works.
  cd "${ROOTDIR}/tools/ossfuzz"
  for protoName in yul abiV2 sol sol2 shuffler solRecStructAlias solRoundtrip; do
    LD_LIBRARY_PATH="${DEPS}/lib" "${DEPS}/bin/protoc" "${protoName}"Proto.proto --cpp_out .
  done
}

# libFuzzer itself, built from compiler-rt source against libc++.
#
# The distro libFuzzer runtime (libclang_rt.fuzzer.a shipped with clang) is
# compiled against libstdc++ — every clang we've tried, distro or apt.llvm.org,
# does this. Its internal std::__cxx11 string/iostream symbols then go unresolved
# when linked into our -stdlib=libc++ harnesses (ld.lld: undefined symbol:
# std::__cxx11::basic_string<...>::reserve, std::ios_base::ios_base(), ...). So we
# compile the matching-version libFuzzer sources ourselves with -stdlib=libc++ and
# link the resulting archive via -DLIB_FUZZING_ENGINE instead.
function build_libfuzzer
{
  if [[ -f "${DEPS}/lib/libFuzzer.a" ]]; then
    return
  fi
  local major
  major="$(${CXX} -dumpversion | cut -d. -f1)"
  local src="${DEPS}/src/llvm-fuzzer"
  if [[ ! -d "${src}/.git" ]]; then
    # Sparse, blobless clone of just compiler-rt/lib/fuzzer at the clang version.
    git clone --depth 1 --filter=blob:none --sparse \
      --branch "release/${major}.x" \
      https://github.com/llvm/llvm-project.git "${src}" \
      || git clone --depth 1 --filter=blob:none --sparse \
           --branch main \
           https://github.com/llvm/llvm-project.git "${src}"
    git -C "${src}" sparse-checkout set compiler-rt/lib/fuzzer
  fi
  local fdir="${src}/compiler-rt/lib/fuzzer"
  local obj="${DEPS}/build/libfuzzer"
  mkdir -p "${obj}"
  rm -f "${obj}"/*.o
  # Plain compile — do NOT instrument the engine itself (no -fsanitize=fuzzer*).
  # The platform-specific Darwin/Windows sources are #if-guarded to empty on Linux.
  local f
  for f in "${fdir}"/*.cpp; do
    "${CXX}" ${LIBCXX_CXXFLAGS} -std=c++17 -O2 -g -fno-omit-frame-pointer -fPIC \
      -c "${f}" -o "${obj}/$(basename "${f}" .cpp).o"
  done
  rm -f "${DEPS}/lib/libFuzzer.a"
  ar rcs "${DEPS}/lib/libFuzzer.a" "${obj}"/*.o
}

function build_fuzzers
{
  cd "${BUILDDIR}"
  export CCACHE_DIR="${ROOTDIR}/.ccache"
  export CCACHE_BASEDIR="${ROOTDIR}"
  export CCACHE_NOHASHDIR=1
  mkdir -p "${CCACHE_DIR}"
  cmake "${ROOTDIR}" \
    -DCMAKE_BUILD_TYPE="${CMAKE_BUILD_TYPE:-Release}" \
    -DCMAKE_TOOLCHAIN_FILE="${ROOTDIR}/cmake/toolchains/libfuzzer-native.cmake" \
    -DCMAKE_PREFIX_PATH="${DEPS}" \
    -DBOOST_ROOT="${DEPS}" \
    -DLPM_PREFIX="${DEPS}" -DEVMONE_PREFIX="${DEPS}" \
    -DLIB_FUZZING_ENGINE="${DEPS}/lib/libFuzzer.a" \
    -DCMAKE_C_COMPILER_LAUNCHER=ccache -DCMAKE_CXX_COMPILER_LAUNCHER=ccache
  ccache -z
  make ossfuzz ossfuzz_proto ossfuzz_abiv2 -j"$(nproc)"
  ccache -s
}

build_boost
build_protobuf_and_lpm
build_evmone_standalone
build_libfuzzer
generate_protobuf_bindings
build_fuzzers
